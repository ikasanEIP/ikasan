public class MappingConfigurationImportException extends Exception
{

    public MappingConfigurationImportException(String message)
    {
        super(message);
    }

    public MappingConfigurationImportException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public MappingConfigurationImportException(Throwable cause)
    {
        super(cause);
    }

    public MappingConfigurationImportException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
    {
        super(message, cause, enableSuppression, writableStackTrace);
    }


}
