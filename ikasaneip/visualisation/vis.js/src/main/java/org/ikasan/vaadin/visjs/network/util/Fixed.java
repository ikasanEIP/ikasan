package org.ikasan.vaadin.visjs.network.util;

public class Fixed {
  boolean x;
  boolean y;

  public boolean isX() {
    return x;
  }

  public void setX(final boolean x) {
    this.x = x;
  }

  public boolean isY() {
    return y;
  }

  public void setY(final boolean y) {
    this.y = y;
  }

}
